package string;

public class Substring {

	
	public  static void main (String args[]) {
		String  cso="priteshporwal";
		System.out.println(cso.substring(1,5));
		System.out.println(cso.toUpperCase());
		System.out.println(cso.toLowerCase());
		System.out.println(cso.charAt(0));
		System.out.println(cso.length());
		
	}
}
